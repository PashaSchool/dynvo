def test_birth_carries_member_files_and_lineage():
    os.environ[MEGA_DECOMP_ARM_ENV] = "1"
    devs, pfs, ufs, flows, ri = _netsec_mint_scene()
    grain = _grain([], pfs, ri)
    tele = run_mega_pf_nav_rehome(devs, pfs, ufs, flows, ri, Ctx(),
                                  grain_index=grain)
    assert tele["pfs_minted"] == 1
    birth = next(p for p in pfs if getattr(p, "split_from", None))
    assert birth.split_from == "netsec"                     # lineage
    assert birth.member_files, "born PF must carry member_files"
    mf_paths = {m.path for m in birth.member_files}
    assert mf_paths == set(birth.paths)                     # real residents
    # single-point ledger: released == claimed, no double count
    led = tele.get("birth_ledger") or {}
    assert led.get("double_counted") == 0
    assert led.get("unreleased_claims") == 0
    # the whole-rehomed dev's file LEFT the source row (no double count)
    src = next(p for p in pfs if p.name == "netsec")
    assert "backend/routers/admin.py" not in src.paths
    assert "conservation_violations" not in tele



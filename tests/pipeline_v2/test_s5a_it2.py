def test_probe_banner_classes(tmp_path):
    sp = _wfile(tmp_path, "sdk/hooksRetrieve.ts", SPEAKEASY)
    ov = _wfile(tmp_path, "api/client.ts", ORVAL)
    ag = _wfile(tmp_path, "gen/schema.ts", AT_GEN)
    hand = _wfile(tmp_path, "app/page.tsx", PLAIN)
    probe = GeneratedContentProbe(str(tmp_path))
    assert probe.is_generated(sp) is True        # Go-spec one-line class
    assert probe.is_generated(ov) is True        # two-line orval class
    assert probe.is_generated(ag) is True        # @generated marker
    assert probe.is_generated(hand) is False     # hand-authored survives
    assert probe.is_generated("missing/file.ts") is False  # I/O fail-open
    # filename convention still answers without content
    assert probe.is_generated("x/y_pb2.py") is True


def test_probe_banner_must_be_in_head(tmp_path):
    # the marker text buried deep in the file (a docs page QUOTING a
    # banner) is not a banner — only the head window counts.
    rel = "docs/example.ts"

def test_strip_armed_kills_sdk_flows_off_keeps_them(tmp_path):
    """The novu class, minimally: a dev of banner-generated .ts files whose
    flows enter through them. OFF → v1 filename predicate keeps them
    (byte-ident); ARMED → files strip, the flow and the dev die."""
    gen = [_wfile(tmp_path, f"libs/sdk/src/react-query/op{i}.ts", SPEAKEASY)
           for i in range(3)]
    hand = _wfile(tmp_path, "apps/api/src/app/agents/agents.controller.ts",
                  PLAIN)
    fl_g = _Fl("f-gen", [gen[0], gen[1]])
    fl_h = _Fl("f-hand", [hand])
    sdk_dev = _F("internal-sdk", gen, [fl_g])
    api_dev = _F("agents-api", [hand], [fl_h])
    # OFF leg — MECHANICAL flip migration (2026-07-19 S*-pack, KEY_SCHEMA
    # 32): the v1 filename-only world is now the explicit kill-switch
    # (unset arms the content-marker probe).
    os.environ[GENERATED_CONTENT_ENV_FLAG] = "0"
    feats = [sdk_dev, api_dev]
    flows = [fl_g, fl_h]
    stats = strip_generated_paths(feats, flows, repo_root=str(tmp_path))
    assert stats["paths_removed"] == 0 and stats["features_dropped"] == 0
    assert sdk_dev in feats and fl_g in flows
    # ARMED: the generated dev + its flow die at the source
    os.environ[GENERATED_CONTENT_ENV_FLAG] = "1"
    assert generated_content_marker_enabled()
    gen2 = [_wfile(tmp_path, f"libs/sdk2/src/op{i}.ts", SPEAKEASY)
            for i in range(3)]
    fl_g2 = _Fl("f-gen2", [gen2[0], gen2[1]])
    fl_h2 = _Fl("f-hand2", [hand])
    sdk2 = _F("internal-sdk-2", gen2, [fl_g2])
    api2 = _F("agents-api-2", [hand], [fl_h2])
    feats2 = [sdk2, api2]
    flows2 = [fl_g2, fl_h2]
    stats2 = strip_generated_paths(feats2, flows2, repo_root=str(tmp_path))
    assert stats2.get("content_marker") == 1
    assert stats2["paths_removed"] > 0
    assert sdk2 not in feats2          # emptied dev dropped
    assert fl_g2 not in flows2         # SDK flow died at the source
    assert api2 in feats2 and fl_h2 in flows2   # hand-authored intact


